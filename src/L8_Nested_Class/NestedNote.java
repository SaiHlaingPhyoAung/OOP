package L8_Nested_Class;

public class NestedNote {
    /*
    Java ဘာသာရပ်တွင် Class တစ်ခုအတွင်း အခြားသော Class များအား ရေးသားနိုင်ပြီး ၎င်းတိုအား Nested Class ဟု ခေါ်ကြပါသည်။

    Nested Class များအား အဓိကအားဖြင့် Static နှင့် Non-Static ဟု နှစ်မျိုးနှစ်စားခွဲခြားနိုင်ပြီး၊ Static အမျိုးအစားအား Static Nested Class ဟု ခေါ်ဆို၍၊
    non-static အမျိုးအစားများအား Inner Class ဟု ခေါ်ဆိုပါသည်။

    Nested Class များသည် ၄င်းအားထည့်သွင်း ရေးသားထားသော Class များ၏ Member တစ်မျိုးဖြစ်ပါသည်။

    Java ဘာသာရပ်တွင် Block အတွင်း တွင်လည်း Class များအား ရေးသားနိုင်ပြီး ၎င်းတိုအား Local Class ဟု ခေါ်ပါသည်။
     */

    /*
                                   Nested Class
                     |                  |                |
          Static nested class      Inner Class        Local Class
                              (non static nested class)
     */
}
